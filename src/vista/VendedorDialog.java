    private void llenarFormulario(Vendedor vendedor) {
        if (vendedor != null) {
            txtId.setText(String.valueOf(vendedor.getId()));
            txtDni.setText(vendedor.getDni());
            txtNombres.setText(vendedor.getNombres());
            txtTelefono.setText(vendedor.getTelefono());
            cmbEstado.setSelectedItem(Estado.getByKey(vendedor.getEstado()).getValue());
            txtUsername.setText(vendedor.getUsername());
            txtPassword.setText(vendedor.getPassword());
        }
    }
    
    private void llenarComboBoxEstado() {
        cmbEstado.addItem(Estado.ACTIVO.getValue());
        cmbEstado.addItem(Estado.INACTIVO.getValue());
    }

