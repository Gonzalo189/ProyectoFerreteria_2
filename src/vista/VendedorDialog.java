    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Connection con = Connector.getConnection();
        VendedorDao vdao = new VendedorDaoImpl(con);

        boolean esNuevo = txtId.getText().equals("");
        Integer id = esNuevo ? null : Integer.valueOf(txtId.getText());
        String dni = txtDni.getText();
        String nombre = txtNombres.getText();
        String telefono = txtTelefono.getText();
        Integer estado = Estado.getByValue(cmbEstado.getSelectedItem().toString()).getKey();
        String username = txtUsername.getText();
        String password = txtPassword.getText();

        Vendedor v = new Vendedor();
        v.setId(id);
        v.setDni(dni);
        v.setNombres(nombre);
        v.setTelefono(telefono);
        v.setEstado(estado);
        v.setUsername(username);
        v.setPassword(password);

        try {
            if (esNuevo) {
                vdao.agregar(v);
            } else {
                vdao.actualizar(v);
            }
            this.dispose();

        } catch (FerreteriaException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());

        }
    }//GEN-LAST:event_btnGuardarActionPerformed

