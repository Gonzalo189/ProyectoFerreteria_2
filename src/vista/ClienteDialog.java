    private void llenarFormulario(Cliente cliente) {
        if (cliente != null) {
            txtId.setText(String.valueOf(cliente.getId()));
            txtDni.setText(cliente.getDni());
            txtNombres.setText(cliente.getNombres());
            txtDirreccion.setText(cliente.getDirreccion());
            cmbEstado.setSelectedItem(Estado.getByKey(cliente.getEstado()).getValue());

        }
    }

    private void llenarComboBoxEstado() {
        cmbEstado.addItem(Estado.ACTIVO.getValue());
        cmbEstado.addItem(Estado.INACTIVO.getValue());
    }

