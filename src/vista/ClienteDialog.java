    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Connection con = Connector.getConnection();
        ClienteDao cdao = new ClienteDaoImpl(con);

        boolean esNuevo = txtId.getText().equals("");
        Integer id = esNuevo ? null : Integer.valueOf(txtId.getText());
        String dni = txtDni.getText();
        String nombre = txtNombres.getText();
        String dirrecion = txtDirreccion.getText();
        Integer estado = Estado.getByValue(cmbEstado.getSelectedItem().toString()).getKey();


        Cliente c = new Cliente();
        c.setId(id);
        c.setDni(dni);
        c.setNombres(nombre);
        c.setDirreccion(dirrecion);
        c.setEstado(estado);

        try {
            if (esNuevo) {
                cdao.agregar(c);
            } else {
                cdao.actualizar(c);
            }
            this.dispose();

        } catch (FerreteriaException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());

        }
    }//GEN-LAST:event_btnGuardarActionPerformed

