    private void llenarFormulario(Producto producto) {
        if (producto != null) {
            txtId.setText(String.valueOf(producto.getId()));
            txtNombre.setText(producto.getNombre());
            txtPrecio.setText(String.valueOf(producto.getPrecio()));
            txtStock.setText(String.valueOf(producto.getStock()));
            cmbEstado.setSelectedItem(Estado.getByKey(producto.getEstado()).getValue());
        }
    }
    
    private void llenarComboBoxEstado() {
        cmbEstado.addItem(Estado.ACTIVO.getValue());
        cmbEstado.addItem(Estado.INACTIVO.getValue());
    }

