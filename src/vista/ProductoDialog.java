    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Connection con = Connector.getConnection();
        ProductoDao pdao = new ProductoDaoImpl(con);
        
        boolean esNuevo = txtId.getText().equals("");
        Integer id = esNuevo ? null : Integer.valueOf(txtId.getText());
        String nombre = txtNombre.getText();
        Double precio = Double.valueOf(txtPrecio.getText());
        Integer stock = Integer.valueOf(txtStock.getText());
        Integer estado = Estado.getByValue(cmbEstado.getSelectedItem().toString()).getKey();
        
        Producto p = new Producto();
        p.setId(id);
        p.setNombre(nombre);
        p.setPrecio(precio);
        p.setStock(stock);
        p.setEstado(estado);
        
        try {
            if (esNuevo) {
                pdao.agregar(p);
            } else {
                pdao.actualizar(p);
            }
            this.dispose();
            
        } catch (FerreteriaException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

